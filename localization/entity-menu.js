cor_kr.getEntityMenuNames = function() {
    let names = []
    for (let i = 0; i < Object.keys(cor_kr.entity_menu).length; i++) {
        names.push(["ENTITY::'"+Object.keys(cor_kr.entity_menu)[i]+"'", "개체::'"+cor_kr.entity_menu[Object.keys(cor_kr.entity_menu)[i]].name+"'"])
    }
    names = Object.fromEntries(names)
    return Object.assign(env.localization['strings'], names)
}

cor_kr.getEntityMenuNames();

